function openBioAction(name, position, description) {
    $("#bio").fadeIn();
    $("#cover").fadeIn();
    $("#bio h2").html(name);
    $("#bio h3").html(position);
    $("#bio p").html(description);
}

function makeOpenBioCallback(profileRef, position, description) {
    return function() {
        var profile = SVYProfiles.get(profileRef);
        openBioAction(profile.name, position, SVYProfiles.description(profileRef, description));
    };
}

