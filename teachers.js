function normalizeProfileName(name) {
    return String(name || "").replace(/\s+/g, " ").trim().toLowerCase();
}

var SVYProfiles = window.SVYProfiles || {
    profilesById: {},
    profilesByName: {},
    normalizeName: normalizeProfileName,
    register: function(profile) {
        if (!profile || !profile.id || !profile.name) {
            throw new Error("SVY profile requires unique id and name");
        }
        if (this.profilesById[profile.id]) {
            throw new Error("Duplicate SVY profile id: " + profile.id);
        }
        this.profilesById[profile.id] = Object.assign({}, profile);
        var key = this.normalizeName(profile.name);
        if (this.profilesByName[key] && this.profilesByName[key].id !== profile.id) {
            this.profilesByName[key] = null;
        } else {
            this.profilesByName[key] = this.profilesById[profile.id];
        }
        return this.profilesById[profile.id];
    },
    registerLegacy: function(profile) {
        if (!profile || !profile.name) {
            return null;
        }
        var key = this.normalizeName(profile.name);
        if (profile.id) {
            if (this.profilesById[profile.id] && this.profilesById[profile.id].name !== profile.name) {
                throw new Error("Duplicate SVY profile id: " + profile.id);
            }
            this.profilesById[profile.id] = Object.assign({}, this.profilesById[profile.id] || {}, profile);
            this.profilesByName[key] = this.profilesById[profile.id];
            return this.profilesById[profile.id];
        }
        if (this.profilesByName[key]) {
            this.profilesByName[key] = Object.assign({}, this.profilesByName[key], profile);
            return this.profilesByName[key];
        }
        this.profilesByName[key] = Object.assign({}, profile);
        return this.profilesByName[key];
    },
    registerMany: function(profiles) {
        if (!profiles) {
            return;
        }
        for (var i = 0; i < profiles.length; i++) {
            this.registerLegacy(profiles[i]);
        }
    },
    get: function(profileRef) {
        if (this.profilesById[profileRef]) {
            return this.profilesById[profileRef];
        }
        var profile = this.profilesByName[this.normalizeName(profileRef)];
        if (profile === null) {
            throw new Error("Ambiguous SVY profile name, use profile id: " + profileRef);
        }
        return profile || { id: "", name: profileRef || "", imgurl: "noimage.png" };
    },
    imagePath: function(profileRef, options) {
        var profile = this.get(profileRef);
        var basePath = options && options.basePath ? options.basePath : "images/teamphoto/";
        return basePath + (profile.imgurl || "noimage.png");
    },
    profileUrl: function(profileRef) {
        var profile = this.get(profileRef);
        return "/profile.html?id=" + encodeURIComponent(profile.id || profileRef || profile.name);
    },
    description: function(profileRef, fallback) {
        var profile = this.get(profileRef);
        return profile.description || fallback || "";
    }
};

